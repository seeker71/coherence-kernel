#!/usr/bin/env node
// Materialize the revision-pinned 111-row substantive repair into the primary
// 10,000 x 13 NL carrier. This is deterministic compilation of committed
// evidence: it performs no network access and invokes no Python.

import { createHash } from "node:crypto";
import { readFileSync, writeFileSync } from "node:fs";

const base = "cognition/concept-nl-semantic-13";
const tablePath = `${base}-omw.tsv`;
const sourcesPath = `${base}-sources.dat`;
const offsetsPath = `${base}-offsets.fk`;
const metadataPath = `${base}-metadata.fk`;
const manifestPath = `${base}-source-manifest.txt`;
const repairPath = "cognition/concept-10000-substantive-repair-111-nl.tsv";
const repairSourcesPath = "cognition/concept-10000-substantive-repair-111-nl-sources.dat";
const codes = ["en", "id", "es", "fr", "pt-br", "sw", "de", "ru", "zh", "ja", "ar", "hi", "tr"];
const digest = (value) => createHash("sha256").update(value).digest("hex");

const original = readFileSync(tablePath, "utf8");
const lines = original.trimEnd().split("\n");
const header = lines.shift();
if (header !== `synset\t${codes.join("\t")}` || lines.length !== 10000)
  throw new Error("primary NL table shape is not 10,000 x 13");
const rows = lines.map((line, id) => {
  const fields = line.split("\t");
  if (fields.length !== 14) throw new Error(`primary row ${id} has ${fields.length} fields`);
  return fields;
});

const sourceBytes = Buffer.from(readFileSync(sourcesPath));
if (sourceBytes.length !== 130000) throw new Error("primary source carrier is not 130,000 bytes");
const repairText = readFileSync(repairPath, "utf8");
const repairLines = repairText.trimEnd().split("\n");
const repairSources = Buffer.from(readFileSync(repairSourcesPath));
if (repairLines.length !== 111 || repairSources.length !== 1443)
  throw new Error("repair overlay shape is not 111 x 13");

const seen = new Set();
repairLines.forEach((line, slot) => {
  const fields = line.split("\t");
  if (fields.length !== 15) throw new Error(`repair row ${slot} has ${fields.length} fields`);
  const id = Number(fields[0]);
  if (!Number.isInteger(id) || id < 0 || id >= 10000 || seen.has(id))
    throw new Error(`invalid or duplicate repair id ${fields[0]}`);
  seen.add(id);
  rows[id] = [fields[1], ...fields.slice(2)];
  for (let lens = 0; lens < 13; lens++) sourceBytes[id * 13 + lens] = repairSources[slot * 13 + lens];
});

const table = `${header}\n${rows.map((row) => row.join("\t")).join("\n")}\n`;
const tableHash = digest(table);
const sourcesHash = digest(sourceBytes);
const repairHash = digest(repairText);
const repairSourcesHash = digest(repairSources);
const rankedHash = digest(readFileSync("model/concept-10000-ranked.dat"));
const offsets = [Buffer.byteLength(`${header}\n`)];
for (const row of rows) offsets.push(offsets.at(-1) + Buffer.byteLength(`${row.join("\t")}\n`));
const offsetsText = `; concept-nl-semantic-13-offsets.fk — generated byte offsets for 10k canonical rows.\n` +
  `; witnessed: 2026-07-18 -> 10,000 primary rows, including 111 attributed substantive repairs\n` +
  `; preludes: form/form-stdlib/core.fk\n(do\n  (defn cnl13-offsets () (list\n` +
  `${offsets.map((n) => `    ${n}`).join("\n")}))\n)\n`;

let metadata = readFileSync(metadataPath, "utf8")
  .replace(/; concept-nl-semantic-13-metadata\.fk[^\n]*/, "; concept-nl-semantic-13-metadata.fk — generated provenance for the canonical primary surface.")
  .replace(/; witnessed:[^\n]*/, "; witnessed: 2026-07-18 -> 130,000 present cells; 111 substantive repairs materialized")
  .replace(/"[0-9a-f]{64}"\n          10000/, `"${tableHash}"\n          10000`)
  .replace("wikt-then-dedicated-wordnet-then-cldr-then-machine-first-hit",
    "base-omw-then-revision-pinned-substantive-repair-111")
  .replace(/"[0-9a-f]{64}"\)\)/, `"${sourcesHash}"))`);
const localeRows = codes.map((code, lens) => {
  const columnBytes = Buffer.from(`${rows.map((row) => row[lens + 1]).join("\n")}\n`);
  const sourceCode = ["eng", "ind", "spa", "fra", "por", "swa", "deu", "rus", "cmn", "jpn", "arb", "hin", "tur"][lens];
  return `    (list "${code}" "${sourceCode}" 10000 0 "${digest(columnBytes)}" "canonical-column-sha256-unreviewed")`;
});
metadata = metadata.replace(/  \(defn cnl13-locale-meta-rows \(\) \(list\n[\s\S]*?\n  \)\)\n/,
  `  (defn cnl13-locale-meta-rows () (list\n${localeRows.join("\n")}\n  ))\n`);
if (!metadata.includes("cnl13-substantive-repair-meta")) {
  metadata = metadata.replace("  (defn cnl13-machine-source-meta ()",
    `  (defn cnl13-substantive-repair-meta ()\n` +
    `    (list 111 1443 "${repairHash}" "${repairSourcesHash}"\n` +
    `          "frequency-observed-and-google-machine-unreviewed"))\n` +
    `  (defn cnl13-machine-source-meta ()`);
}

let manifest = readFileSync(manifestPath, "utf8")
  .replace(/^label=.*$/m, "label=base-omw-then-revision-pinned-substantive-repair-111")
  .replace(/^[0-9a-f]{64}  concept-nl-semantic-13-omw\.tsv$/m,
    `${tableHash}  concept-nl-semantic-13-omw.tsv`)
  .replace(/^[0-9a-f]{64}  concept-nl-semantic-13-sources\.dat$/m,
    `${sourcesHash}  concept-nl-semantic-13-sources.dat`)
  .replace(/^[0-9a-f]{64}  model\/concept-10000-ranked\.dat$/m,
    `${rankedHash}  model/concept-10000-ranked.dat`);
const repairManifest = `${repairHash}  cognition/concept-10000-substantive-repair-111-nl.tsv\n` +
  `${repairSourcesHash}  cognition/concept-10000-substantive-repair-111-nl-sources.dat\n` +
  `${digest(readFileSync("model/concept-10000-substantive-repair-evidence.jsonl"))}  model/concept-10000-substantive-repair-evidence.jsonl\n`;
if (!manifest.includes("cognition/concept-10000-substantive-repair-111-nl.tsv"))
  manifest += repairManifest;

writeFileSync(tablePath, table);
writeFileSync(sourcesPath, sourceBytes);
writeFileSync(offsetsPath, offsetsText);
writeFileSync(metadataPath, metadata);
writeFileSync(manifestPath, manifest);

const counts = Object.fromEntries(["F", "W", "D", "C", "G", "0"].map((code) =>
  [code, [...sourceBytes].filter((byte) => byte === code.charCodeAt(0)).length]));
process.stdout.write(`${JSON.stringify({ rows: rows.length, repairs: seen.size, cells: sourceBytes.length,
  tableSha256: tableHash, sourcesSha256: sourcesHash, sourceCounts: counts })}\n`);
