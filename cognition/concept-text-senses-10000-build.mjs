#!/usr/bin/env node
// Build the explicit WordNet-sense surface used by Form-native text detection.
// Usage: node cognition/concept-text-senses-10000-build.mjs /path/to/wordnet-3.1/dict

import { createHash } from "node:crypto";
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const repo = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const dict = process.argv[2] ? resolve(process.argv[2]) : "";
if (!dict) {
  console.error("usage: node cognition/concept-text-senses-10000-build.mjs /path/to/wordnet-3.1/dict");
  process.exit(2);
}

const sha256 = (bytes) => createHash("sha256").update(bytes).digest("hex");
const clean = (text) => text.replace(/[\t\r\n]+/g, " ");
const posFromSenseKey = (key) => {
  const kind = key.slice(key.indexOf("%") + 1, key.indexOf("%") + 2);
  return kind === "1" ? "n" : kind === "2" ? "v" : (kind === "3" || kind === "5") ? "a" : "r";
};

const glosses = new Map();
for (const [file, pos] of [["data.noun", "n"], ["data.verb", "v"], ["data.adj", "a"], ["data.adv", "r"]]) {
  const text = readFileSync(join(dict, file), "utf8");
  for (const line of text.split("\n")) {
    if (!/^[0-9]{8} /.test(line)) continue;
    const bar = line.indexOf(" | ");
    const fields = line.slice(0, bar < 0 ? line.length : bar).trim().split(/ +/);
    const type = fields[2] === "s" ? "s" : pos;
    glosses.set(`${pos}${fields[0]}`, { type, gloss: clean(bar < 0 ? "" : line.slice(bar + 3).trimEnd()) });
  }
}

const byLemma = new Map();
const senseText = readFileSync(join(dict, "index.sense"), "utf8");
for (const line of senseText.split("\n")) {
  if (!line) continue;
  const [key, offset, senseNumber, tagCount] = line.split(" ");
  if (!key || !offset) continue;
  const lemma = key.slice(0, key.indexOf("%"));
  const pos = posFromSenseKey(key);
  const definition = glosses.get(`${pos}${offset}`) || { type: pos, gloss: "" };
  const row = [definition.type, offset, Number(senseNumber), Number(tagCount), definition.gloss];
  if (!byLemma.has(lemma)) byLemma.set(lemma, []);
  byLemma.get(lemma).push(row);
}

const semanticIndex = readFileSync(join(repo, "model/concept-semantics-10000-index.dat"), "utf8");
const semanticPayload = readFileSync(join(repo, "model/concept-semantics-10000-payload.dat"), "utf8");
if (semanticIndex.length !== 350000) throw new Error(`semantic index length ${semanticIndex.length}, expected 350000`);

const rows = [];
const offsets = [0];
let byteOffset = 0;
let totalSenses = 0;
let mappedAnchors = 0;
let ambiguousAnchors = 0;
let maxSenses = 0;
let sourceCountMismatch = 0;
for (let id = 0; id < 10000; id++) {
  const record = semanticIndex.slice(id * 35, id * 35 + 35);
  const method = Number(record.slice(0, 1));
  let senses = [];
  if (method > 0) {
    mappedAnchors++;
    const payloadOffset = Number(record.slice(1, 9));
    const lemmaLength = Number(semanticPayload.slice(payloadOffset, payloadOffset + 2));
    const lemma = semanticPayload.slice(payloadOffset + 2, payloadOffset + 2 + lemmaLength);
    senses = byLemma.get(lemma) || [];
    const projectedCount = Number(record.slice(23, 26));
    if (senses.length !== projectedCount) sourceCountMismatch++;
  }
  if (senses.length > 1) ambiguousAnchors++;
  if (senses.length > maxSenses) maxSenses = senses.length;
  totalSenses += senses.length;
  const fields = [String(id), String(senses.length)];
  for (const [type, offset, number, tags, gloss] of senses) {
    fields.push(`${type}${offset}`, type, String(number), String(tags), gloss);
  }
  const row = `${fields.join("\t")}\n`;
  rows.push(row);
  byteOffset += Buffer.byteLength(row);
  offsets.push(byteOffset);
}
if (sourceCountMismatch !== 0) throw new Error(`${sourceCountMismatch} anchors disagree with the projected WordNet sense count`);

const dataBytes = Buffer.from(rows.join(""));
const dataPath = join(repo, "cognition/concept-text-senses-10000.tsv");
const offsetsPath = join(repo, "cognition/concept-text-senses-10000-offsets.fk");
writeFileSync(dataPath, dataBytes);
writeFileSync(offsetsPath,
`; concept-text-senses-10000-offsets.fk -- generated byte offsets for 10,000 sense rows.\n` +
`; generated by cognition/concept-text-senses-10000-build.mjs\n` +
`; witnessed: 2026-07-18 -> ${offsets.length} offsets, final byte ${byteOffset}\n` +
`; preludes: form/form-stdlib/core.fk\n` +
`(do (defn cts10-offsets () (list ${offsets.join(" ")})) 0)\n`);

const inputHash = (name) => sha256(readFileSync(join(dict, name)));
const metadataPath = join(repo, "cognition/concept-text-senses-10000-metadata.fk");
writeFileSync(metadataPath,
`; concept-text-senses-10000-metadata.fk -- pinned explicit WordNet ambiguity surface.\n` +
`; witnessed: 2026-07-18 -> ${totalSenses} senses across ${mappedAnchors} mapped anchors.\n` +
`; preludes: form/form-stdlib/core.fk\n` +
`(do\n` +
`  (defn cts10-count () 10000)\n` +
`  (defn cts10-mapped-anchors () ${mappedAnchors})\n` +
`  (defn cts10-total-senses () ${totalSenses})\n` +
`  (defn cts10-ambiguous-anchors () ${ambiguousAnchors})\n` +
`  (defn cts10-max-senses () ${maxSenses})\n` +
`  (defn cts10-data-path () "cognition/concept-text-senses-10000.tsv")\n` +
`  (defn cts10-data-sha256 () "${sha256(dataBytes)}")\n` +
`  0)\n`);

const manifestPath = join(repo, "cognition/concept-text-senses-10000-source-manifest.txt");
writeFileSync(manifestPath,
`Explicit concept sense surface source manifest\n` +
`witnessed: 2026-07-18\n\n` +
`source https://wordnetcode.princeton.edu/wn3.1.dict.tar.gz\n` +
`archive_sha256 3f7d8be8ef6ecc7167d39b10d66954ec734280b5bdcd57f7d9eafe429d11c22a\n` +
`index.sense_sha256 ${inputHash("index.sense")}\n` +
`data.noun_sha256 ${inputHash("data.noun")}\n` +
`data.verb_sha256 ${inputHash("data.verb")}\n` +
`data.adj_sha256 ${inputHash("data.adj")}\n` +
`data.adv_sha256 ${inputHash("data.adv")}\n` +
`semantic_index_sha256 ${sha256(Buffer.from(semanticIndex))}\n` +
`semantic_payload_sha256 ${sha256(Buffer.from(semanticPayload))}\n` +
`output cognition/concept-text-senses-10000.tsv\n` +
`output_sha256 ${sha256(dataBytes)}\n` +
`anchors 10000\nmapped_anchors ${mappedAnchors}\nambiguous_anchors ${ambiguousAnchors}\n` +
`total_senses ${totalSenses}\nmax_senses ${maxSenses}\n` +
`license model/concept-semantics-10000-WORDNET-LICENSE.txt\n`);

console.log(JSON.stringify({ anchors: 10000, mappedAnchors, ambiguousAnchors, totalSenses, maxSenses, bytes: byteOffset, sha256: sha256(dataBytes) }));
